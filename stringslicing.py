#String Slicing

str = "I Love Programming"
slice1 = str[0:1]
print(slice1)       #output is 'I'

slice2 = str[2:6]
print(slice2)       #output is 'Love'           

slice3 = str[7:18]
print(slice3)       #output is 'programming'

slice4 = str[17::-1]
print(slice4)       #output will be reversing the String
                    #output is 'gnimmargorP evoL I'
