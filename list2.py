#Append the elements to a List1
List1 = ["I","Love","Python"]
List1.append("Programming")
print(List1)

#changing elements in a List1
List1[2] = "Java"
print(List1)            #replace the element 'Python' with 'Java'

#printing all elements in a List1 using for loop
for i in List1:
     print(i,end=" ")
print("\n")

#Finding length of a List1
print("Length of the List1 is: ",end="")
print(len(List1))           #prints the length of the List1