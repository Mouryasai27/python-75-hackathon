
#adding and updating the set

set_1 = {1,2,3,4,5}
print(set_1)        #printing the set_1


set_1.add(6)
print(set_1)        #'add' will append only ONE element to the set

set_1.update({7,8,9})
print(set_1)        #'update' will add multiple values to the set

set_1.update([10,11])
print(set_1)        #same functions as above code snippet




