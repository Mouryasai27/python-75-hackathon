#Logging in Python

#First of all we need toimport the 'logging'package in Python inorder to use it

import logging

#It is having 5 standard levels indicating the severity of events and they are used as follows

logging.warning('displays warning message')       #warning message
logging.debug('displays debug message')         #debug message
logging.error('displays error message')         #error message
logging.info('displays info message')          #info message
logging.critical('displays critical message')      #critical message
